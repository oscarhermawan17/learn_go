package main

import "fmt"

func main() {
	fmt.Println("Oscar")
	fmt.Println("Oscar Hermawan")


	fmt.Println(len("Oscar Hermawan")) // Getting length of string
	fmt.Println("Oscar Hermawan"[0]) // Getting Byte of char "O" (Uppercase)
	fmt.Println("Oscar Hermawan"[1]) // Getting Byte of char "s" (lowercase)
}